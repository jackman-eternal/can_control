void SetpMotor_SetStep(int id, int steps)	
{
    motor[id].target=motor[0].step+steps*2;
}

void SetpMotor_SetSpeed(int id, int speed)
{
    motor[id].div=speed;
}


void TIM3_IRQHandler(void)
{
    static int timer=0;
	  //int dirpin,steppin;
    if(TIM_GetITStatus(TIM3,TIM_IT_Update) != RESET)
			{
        TIM_ClearITPendingBit(TIM3,TIM_IT_Update);
				#define _m(id,steppin,dirpin)\
        if(timer%motor[0].div==0)\
					{\
            if(motor[0].step<motor[0].target)\
							{\
                dirpin=0;\
                steppin=timer/motor[0].div&1;\
                motor[0].step++;\
              }\
							else if(motor[0].step>motor[0].target)\
							{\
                dirpin=1;\
                steppin=timer/motor[0].div&1;\
                motor[0].step--;\
              }\
        }
       _m(0,PAout(6),PAout(7));//PA0输出脉冲   PA7控制方向
       timer++;
       }
}

